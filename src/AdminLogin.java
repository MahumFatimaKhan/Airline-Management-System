    void checkData(String Adminpassword){
        if(!Adminpassword.equals("")){
         try {
            RandomAccessFile raf=new RandomAccessFile(f+"\\Adminpw.txt","rw");
         
            String pw=raf.readLine().substring(9);
            if(Adminpassword.equals(pw)) {
                dispose();
                ChooseAdmin ad1=new ChooseAdmin();
                ad1.setLocationRelativeTo(null);
                ad1.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Incorrect Password");
            }
       } catch (FileNotFoundException ex) {
           Logger.getLogger(AdminLogin.class.getName()).log(Level.SEVERE, null, ex);
       } catch (IOException ex) {
           Logger.getLogger(AdminLogin.class.getName()).log(Level.SEVERE, null, ex);
       }
        }
        else{
                JOptionPane.showMessageDialog(rootPane, "Please fill in the required field");
        }
    }

